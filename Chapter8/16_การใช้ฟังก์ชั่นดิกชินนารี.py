d = {'a': 1, 'b': 2, 'c': 3}

# 1. ใช้ get() เพื่อดึงค่า ถ้าไม่มี key ให้คืนค่าเริ่มต้น
print(d.get('a', 0))  # ผลลัพธ์: 1
print(d.get('x', 0))  # ผลลัพธ์: 0

# 2. ใช้ pop() เพื่อนำค่าออก ถ้าไม่มี key ให้คืนค่าเริ่มต้น
print(d.pop('b', -1))  # ผลลัพธ์: 2
print(d)               # ผลลัพธ์: {'a': 1, 'c': 3}

# 3. ใช้ update() เพื่อเพิ่มหรืออัปเดตค่า
d.update({'d': 4, 'a': 10})
print(d)  # ผลลัพธ์: {'a': 10, 'c': 3, 'd': 4}