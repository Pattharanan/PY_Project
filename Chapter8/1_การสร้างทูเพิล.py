# สมาชิกเป็นชนิดเดียวกัน
numbers = (1, 2, 3, 4, 5)
print(numbers)  # (1, 2, 3, 4, 5)


# สมาชิกเป็นชนิดต่างกัน
mixed_tuple = (10, "Hello", 3.14, True)
print(mixed_tuple)  # (10, 'Hello', 3.14, True)

#ถ้าสร้าง tuple ที่มีค่าเดียว ต้องใส่ , ตามหลังค่าด้วยไม่งั้นจะเป็น integer
single_value_tuple = (5,)  
print(single_value_tuple)