#ในลิสต์ my_list จะมีสมาชิกทั้งหมด 5 ตัว โดยมีหมายเลขลำดับดังนี้:
my_list = [10, 20, 30, 40, 50]

# การเข้าถึงสมาชิกจากท้ายสุดของลิสต์
print(my_list[0])  # จะได้ค่า 10
print(my_list[1])  # จะได้ค่า 20
print(my_list[2])  # จะได้ค่า 30
print(my_list[3])  # จะได้ค่า 40
print(my_list[4])  # จะได้ค่า 50
print("-" * 10)
print(my_list[-1])  # จะได้ค่า 50
print(my_list[-2])  # จะได้ค่า 40
print(my_list[-3])  # จะได้ค่า 30
print(my_list[-4])  # จะได้ค่า 20
print(my_list[-5])  # จะได้ค่า 10