# สร้างเซต
my_set = {1, 2, 3, 4, 5}

# การใช้โอเปอเรเตอร์
print(3 in my_set)   # ตรวจสอบว่า 3 อยู่ในเซตหรือไม่ -> True
print(6 not in my_set)  # ตรวจสอบว่า 6 ไม่อยู่ในเซตหรือไม่ -> True

# การใช้ฟังก์ชัน
print(len(my_set))  # หาขนาดของเซต -> 5
print(max(my_set))  # หาค่าสูงสุดในเซต -> 5
print(min(my_set))  # หาค่าต่ำสุดในเซต -> 1
print(sum(my_set))  # หาผลรวมของสมาชิกในเซต -> 15