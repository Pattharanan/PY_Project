A = [1, 3, 5, 7, 9, 11]

# append(x) – เพิ่มค่า x ต่อท้ายลิสต์
A.append(13)  # เพิ่ม 13 ต่อท้ายลิสต์
print(A)  # ผลลัพธ์: [1, 3, 5, 7, 9, 11, 13]

# insert(i, x) – แทรกค่า x ที่ตำแหน่ง i
A.insert(2, 6)  # แทรก 6 ที่ตำแหน่ง index 2
print(A)  # ผลลัพธ์: [1, 3, 6, 5, 7, 9, 11, 13]

# remove(x) – ลบค่าของ x จากลิสต์
A.remove(6)  # ลบค่า 6 ออกจากลิสต์
print(A)  # ผลลัพธ์: [1, 3, 5, 7, 9, 11, 13]