# การรวมลิสต์ด้วย +
A = [1, 3]
B = [2, 4]
C = A + B  # รวม A และ B
print(C)  # ผลลัพธ์: [1, 3, 2, 4]

# การเพิ่มค่าลงในลิสต์ด้วย +=
C += [5, 6, 7, 8]  # เพิ่มสมาชิกใหม่ใน C
print(C)  # ผลลัพธ์: [1, 3, 2, 4, 5, 6, 7, 8]

# การสร้างลิสต์ที่มีค่าซ้ำกันด้วย *
D = [2] * 4  # สร้างลิสต์ที่มีค่า 2 ซ้ำกัน 4 ครั้ง
print(D)  # ผลลัพธ์: [2, 2, 2, 2]