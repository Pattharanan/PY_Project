#การรวมทูเพิลด้วย +
A = (1, 3)
B = (2, 4)
C = A + B
print(C)  # ผลลัพธ์(1, 3, 2, 4)


#การเพิ่มค่าด้วย +=
C += (5, 6, 7, 8)
print(C) # ผลลัพธ์(1, 3, 2, 4, 5, 6, 7, 8)


#การเพิ่มค่าที่มีค่าซ้ำด้วย *
D = (2,) * 4
print(D) # ผลลัพธ์(2, 2, 2, 2)


# ผลรวมของค่าทั้งหมดในทูเพิลด้วย sum()
print(sum(C))  # ผลลัพธ์ 36

# หาค่าสูงสุดด้วย max()
print(max(C))  # ผลลัพธ์ 8

# หาค่าต่ำสุดด้วย min()
print(min(C))  # ผลลัพธ์ 1

# หาค่าต่ำสุดด้วย min()
print(len(C))  # ผลลัพธ์ 8