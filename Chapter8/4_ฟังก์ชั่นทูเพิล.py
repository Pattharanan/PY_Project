A = (1, 2, 3, 3, 3, 4, 5, 5, 5, 6, 7, 8, 8, 9)

# หาตำแหน่งแรกที่ค่า 4 ปรากฏใน A
print(A.index(4))  # ผลลัพธ์: 5

# นับจำนวนครั้งที่ค่า 3 ปรากฏใน A
print(A.count(3))  # ผลลัพธ์: 3

# นับจำนวนครั้งที่ค่า 5 ปรากฏใน A
print(A.count(5))  # ผลลัพธ์: 3

# นับจำนวนครั้งที่ค่า 0 ปรากฏใน A
print(A.count(0))  # ผลลัพธ์: 0

# หาตำแหน่งของค่า 0 ใน A (แต่ไม่มีค่า 0 ใน A)
print(A.index(0))  # ❌ จะเกิด ValueError