temperature = float(input("กรุณาใส่อุณหภูมิ (°C): "))

if temperature >= 30:
    print("อากาศร้อนมาก! ใส่เสื้อผ้าบาง ๆ และดื่มน้ำเยอะ ๆ")
elif temperature >= 20:
    print("อากาศกำลังสบาย! ใส่เสื้อแขนสั้นหรือเสื้อแขนยาวบาง ๆ")
elif temperature >= 10:
    print("อากาศเย็น! ควรใส่เสื้อกันหนาว")
else:
    print("หนาวจัด! ใส่เสื้อกันหนาวหนา ๆ และพกผ้าพันคอ")