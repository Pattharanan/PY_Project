text = "HeLLoWoRLd"  

print()
print("Lower String = ", text.lower())  # จะแปลงเป็นตัวพิมพ์เล็กทั้งหมด
print("Upper String = ", text.upper())  # จะแปลงเป็นตัวพิมพ์ใหญ่ทั้งหมด
print("Capitalize String= ", text.capitalize())  # จะแปลงตัวแรกเป็นพิมพ์ใหญ่ที่เหลือจะเป็นพิมพ์เล็ก
print("SwapCase String = ", text.swapcase())  # จะเป็นการสลับตัวพิมพ์เล็ก-ใหญ่