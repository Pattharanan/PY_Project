text = "helloworld"

print()
print("String is alpha =", text.isalpha())  # (มีแต่ตัวอักษร ไม่มีช่องว่างหรือตัวเลข)
print("String is numeric =", text.isnumeric())  # (มีแต่ตัวเลข)
print("String is lower =", text.islower())  # (ตัวพิมพ์เล็กทั้งหมด)
print("String is space =", text.isspace())  # (ไม่มีแต่ช่องว่าง)