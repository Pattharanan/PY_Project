# จัดรูปแบบข้อความ
name = 'Python'
print('%s' % name)  # แสดงข้อความปกติ
print('|%s|' % name)  # แสดงข้อความภายใน | |
print('|%10s|' % name)  # ชิดขวา กว้าง 10 ตัวอักษร
print('|%-10s|' % name)  # ชิดซ้าย กว้าง 10 ตัวอักษร
print('|%-10c|' % name[0])  # ชิดซ้าย ตัวอักษรเดียว
print('|%10c|' % name[0])  # ชิดขวา ตัวอักษรเดียว