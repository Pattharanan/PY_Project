text = input('Enter your string : ')

# จัดตำแหน่งข้อความตรงกลางในพื้นที่ 10 ตัวอักษร
print('|' + text.center(10) + '|')

# จัดตำแหน่งข้อความชิดซ้ายและเติม '-' ให้ครบ 10 ตัวอักษร
print('|' + text.ljust(10, '*') + '|')

# จัดตำแหน่งข้อความชิดขวาและเติม '#' ให้ครบ 10 ตัวอักษร
print('|' + text.rjust(10, '#') + '|')