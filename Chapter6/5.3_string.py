s = "Python"

print("จำนวน 'Py' ในสตริง :", s.count('Py'))       # 1  -> นับจำนวนครั้งที่ 'Py' ปรากฏในสตริง
print("ตำแหน่งของ 'on':", s.find('on'))        # 4  -> ค้นหาตำแหน่งที่ 'on' ปรากฏครั้งแรกในสตริง
print("ตำแหน่งของ 'th':", s.index('th'))       # 2  -> ค้นหาตำแหน่งที่ 'th' ปรากฏครั้งแรกในสตริง

new_s = s.replace('thon', 'Game')  
print("ผลลัพธ์หลังจากแทนที่:", new_s)  # 'PyGame' -> แทนที่ 'thon' ด้วย 'Game'